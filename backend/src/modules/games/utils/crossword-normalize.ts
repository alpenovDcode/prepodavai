/**
 * Нормализация буквы для сравнения в кроссворде.
 *
 * Что делает:
 *   1. `.toUpperCase()` (в JS для 'ß' даёт 'SS' автоматически).
 *   2. `ẞ → SS` — если строка пришла уже в верхнем регистре, toUpperCase
 *      её не тронет.
 *   3. `Ё → Е` — в русских школьных кроссвордах взаимозаменяемы,
 *      учителя обычно так и оценивают.
 *      Й/И НЕ смешиваем — это самостоятельные буквы русского алфавита.
 *   4. NFD-разложение → снятие combining marks ТОЛЬКО после латинских
 *      букв → NFC-сборка.
 *      • FÁCIL / FACIL, GARÇON / GARCON, SCHÖNE / SCHONE — все считаются
 *        одним словом (учителя генерируют на разных языках, ученики
 *        часто вводят без акцентов);
 *      • Й = И + ̆ (breve) — обе половинки кириллические, regex их
 *        не тронет; финальный NFC вернёт Й в композит.
 *
 * ВАЖНО: эта же логика продублирована в
 * backend/src/templates/games/crossword.html внутри inline-скрипта
 * (браузер не может импортировать TS-модуль). При правке синхронизировать.
 */
export function normalizeLetters(s: string | null | undefined): string {
    let out = String(s || '').toUpperCase();
    // ẞ → SS (на случай если пришло уже в верхнем регистре — toUpperCase
    // сам разворачивает 'ß', но 'ẞ' оставляет как есть).
    out = out.replace(/ẞ/g, 'SS');
    // Ё → Е (Й НЕ трогаем).
    out = out.replace(/Ё/g, 'Е');
    // Снимаем диакритику только с латиницы, кириллицу возвращаем в NFC.
    out = out
        .normalize('NFD')
        .replace(/([A-Z])[̀-ͯ]+/g, '$1')
        .normalize('NFC');
    return out;
}
