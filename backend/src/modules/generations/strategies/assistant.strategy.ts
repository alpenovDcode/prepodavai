const HTML_OUTPUT_RULES = `
ПРАВИЛА ФОРМАТИРОВАНИЯ HTML-ОТВЕТА:
1. Выдавай ТОЛЬКО HTML-фрагмент — без <!DOCTYPE>, <html>, <head>, <body>.
2. Используй семантическую разметку:
   - Параграфы: <p>
   - Заголовки: <h3> (не h1/h2 — они слишком крупные в чате)
   - Жирный акцент: <strong>
   - Курсив: <em>
   - Маркированный список: <ul><li>
   - Нумерованный список: <ol><li>
   - Код/команды: <code>
   - Горизонтальный разделитель: <hr>
3. ЗАПРЕЩЕНО: Markdown-синтаксис (# ## * _ \`\`\`). Только HTML.
4. ЗАПРЕЩЕНО: сопроводительный текст до или после HTML. Ответ начинается и заканчивается тегом.
5. Для структурированных ответов используй карточки:
   <div class="ai-card"><strong>Заголовок</strong><p>Содержание</p></div>
6. Для пошаговых инструкций:
   <ol class="ai-steps"><li><strong>Шаг 1.</strong> Описание</li></ol>
7. Для выделения важного:
   <div class="ai-tip">💡 Совет или важная информация</div>
8. Для примеров готовых текстов/скриптов:
   <blockquote class="ai-quote">Текст примера</blockquote>
`;

@Injectable()
export class AssistantStrategy implements GenerationStrategy {
  supports(type: string): boolean {
    return ['assistant', 'chat'].includes(type);
  }

  async generate(params: GenerationRequestParams): Promise<{ systemPrompt: string; userPrompt: string }> {
    const { prompt: userMsg, systemPrompt: persona } = params;

    const p = PERSONAS[persona as string] ?? PERSONAS['general'];

    const systemPrompt = `
${DesignSystemConfig.PROMPT_MODULES.SYSTEM_INTRO}
Ты — ${p.role}.

${p.instructions}

${DesignSystemConfig.PROMPT_MODULES.MATHJAX_RULES}

${HTML_OUTPUT_RULES}
`;

    const userPrompt = userMsg || 'Привет! Расскажи, чем ты можешь мне помочь?';

    return { systemPrompt, userPrompt };
  }
}
